#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	hhhhh(char a, char b, char c)
{
	ft_putchar(a);
	ft_putchar(b);
	ft_putchar(c);
	if (a != '7')
	{
		ft_putchar(',');
		ft_putchar(' ');
	}
}

void	ft_print_comb(void)
{
	int	a;
	int	b;
	int	c;

	a = 48;
	while (a <= 55)
	{
		b = a + 1;
		while (b <= 56)
		{
			c = b + 1;
			while (c <= 57)
			{
				hhhhh(a, b, c);
				c++;
			}
			b++;
		}
		a++;
	}
}
