#include <unistd.h>

void	ktb(char c)
{
	write(1, &c, 1);
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			ktb((a / 10) + 48);
			ktb((a % 10) + 48);
			ktb(' ');
			ktb((b / 10) + 48);
			ktb((b % 10) + 48);
			if (a != 98)
			{
				ktb(',');
				ktb(' ');
			}
			b++;
		}
		a++;
	}
}
