#include <unistd.h>

void	ktb(char c)
{
	write (1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		ktb('-');
		ktb('2');
		nb = 147483648;
	}
	if (nb >= 0 && nb <= 9)
		ktb(nb + 48);
	if (nb < 0)
	{
		ktb('-');
		nb = -nb;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ktb((nb % 10) + 48);
	}
}
