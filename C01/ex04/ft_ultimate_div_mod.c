void	ft_ultimate_div_mod(int *a, int *b)
{
	int	yass1;
	int	yass2;

	if (*b != 0)
	{
		yass1 = *a / *b;
		yass2 = *a % *b;
		*a = yass1;
		*b = yass2;
	}
}
