int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	p;

	p = 0;
	while ((p < n) && (s1[p] != '\0' && s2[p] != '\0'))
	{
		if (s1[p] != s2[p])
			return (s1[p] - s2[p]);
		++p;
	}
	if (p != n)
	{
		return (s1[p] - s2[p]);
	}
	return (0);
}
