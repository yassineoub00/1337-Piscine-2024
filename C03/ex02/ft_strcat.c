char	*ft_strcat(char *dest, char *src)
{
	int	y;
	int	j;

	y = 0;
	while (dest[y] != '\0')
	{
		y++;
	}
	j = 0;
	while (src[j] != '\0')
	{
		dest[y + j] = src[j];
		j++;
	}
	dest[y + j] = '\0';
	return (dest);
}
