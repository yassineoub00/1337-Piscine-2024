char	*ft_strstr(char *str, char *to_find)
{
	int	a;
	int	s;

	a = 0;
	s = 0;
	while (str[a] != '\0' && to_find[a] == str[a + s])
	{
		if (to_find[s + 1] == '\0')
		{
			return (str + a);
			s++;
		}
		a++;
	}
	return (0);
}
